class Animation:
    """
    Class that animates based on a given sheet (sprite/image) and incorporates time
    >:3 - tainted by William
    """
    def __init__(self, sheet, cooldown):
        self.frame = 0
        self.action = 0
        self.ani_list = sheet
        self.animation_cooldown = cooldown
        self.last_update = pygame.time.get_ticks()


    def update_animation(self):
        """Method that returns animation per frame, based on time."""
        current_time = pygame.time.get_ticks()

        # Checks if it is on last frame
        if current_time - self.last_update >= self.animation_cooldown:
            self.frame += 1
            self.last_update = current_time
            if self.frame >= len(self.ani_list[self.action]):
                self.frame = 0

    def final_product(self):
        """Method that returns the final animation after all frames have been processed."""
        return (self.ani_list[self.action][self.frame]) # Final frame
